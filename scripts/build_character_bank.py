def extract_chinese(text):
    """Extract only Chinese characters from text."""
    return [c for c in text if '\u4e00' <= c <= '\u9fff']


def build_character_bank():
    char_counter = Counter()
    book_char_info = {}

    for title, info in BOOKS_DATA.items():
        if info.get("wordless"):
            continue
        all_chars = info.get("chars", [])
        extra = info.get("extra_chars", [])
        all_chars.extend(extra)
        all_chars = extract_chinese("".join(all_chars))
        unique = sorted(set(all_chars))
        book_char_info[title] = {
            "characters": unique,
            "total_chars": len(unique),
            "status": info.get("status", "未读"),
            "source": info.get("source", "纸质书"),
            "wordless": False
        }
        for c in unique:
            char_counter[c] += 1

    # Build character bank
    chars = {}
    for char, freq in char_counter.most_common():
        books_with_char = []
        for title, info in book_char_info.items():
            if char in info["characters"]:
                books_with_char.append(title)
        chars[char] = {
            "freq": freq,
            "books": books_with_char,
            "status": "未学",
            "learned_date": None
        }

    # ─── Import learning data from FlowUs ───
    flowus_path = "/mnt/d/rex-识字系统/books_from_flowus.json"
    if os.path.exists(flowus_path):
        with open(flowus_path, "r", encoding="utf-8") as f:
            flowus_data = json.load(f)
        for book in flowus_data.get("books", []):
            title = book.get("title", "")
            raw = book.get("learned_chars_raw", "")
            if raw and title in book_char_info:
                learned = [c.strip() for c in raw.replace("、", "，").split("，") if c.strip()]
                learned_chinese = extract_chinese("".join(learned))
                for c in learned_chinese:
                    if c in chars:
                        chars[c]["status"] = "已学"

    total_unique = len(chars)
    learned_count = sum(1 for c in chars.values() if c["status"] == "已学")

    bank = {
        "version": 1,
        "last_updated": "2026-06-30T12:00:00Z",
        "books": book_char_info,
        "chars": chars,
        "log": [],
        "progress": {
            "total_books": len(book_char_info),
            "total_unique_chars": total_unique,
            "learned": learned_count,
            "learning": 0,
            "untouched": total_unique - learned_count
        }
    }

    return bank


